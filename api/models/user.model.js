import mongoose, { Schema } from "mongoose";
// create a schema
const userSchema = new Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

// always make it singular
const User = mongoose.model("User", userSchema);

export default User;